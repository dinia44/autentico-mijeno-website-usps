document.addEventListener("DOMContentLoaded", () => {
  const year = document.getElementById("currentYear");
  const form = document.getElementById("valuationForm");

  if (year) {
    year.textContent = new Date().getFullYear();
  }

  bindLanguageToggle();
  applyLanguage(getSavedLanguage());

  if (!form) return;

  form.addEventListener("submit", (event) => {
    event.preventDefault();

    const data = new FormData(form);
    const website = clean(data.get("website"));
    const name = clean(data.get("nombre"));
    const phone = clean(data.get("telefono"));
    const service = clean(data.get("servicio"));
    const message = clean(data.get("mensaje"));
    const language = getSavedLanguage();
    const meta = translations[language].meta;

    if (website) return;

    const body = [
      language === "es" ? "Hola James y Andrea," : "Hello James and Andrea,",
      "",
      meta.emailIntro,
      "",
      `${translations[language].form.name}: ${name}`,
      `${translations[language].form.phone}: ${phone}`,
      `${translations[language].form.service}: ${service}`,
      `${translations[language].form.message}: ${message || meta.noMessage}`,
    ].join("\n");

    window.location.href = `mailto:${contactEmail}?subject=${encodeURIComponent(meta.subject)}&body=${encodeURIComponent(body)}`;

    const note = document.getElementById("formNote");
    if (note) {
      note.textContent = meta.formNote;
    }
  });
});

function bindLanguageToggle() {
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => {
      const language = button.dataset.lang;
      if (!translations[language]) return;
      localStorage.setItem(languageKey, language);
      applyLanguage(language);
    });
  });
}

function applyLanguage(language) {
  const dictionary = translations[language] || translations.es;

  document.documentElement.lang = language;
  document.title = dictionary.meta.title;
  setMeta('meta[name="description"]', "content", dictionary.meta.description);
  setMeta('meta[property="og:title"]', "content", dictionary.meta.title);
  setMeta('meta[property="og:description"]', "content", dictionary.meta.description);
  setMeta('meta[name="twitter:title"]', "content", dictionary.meta.title);
  setMeta('meta[name="twitter:description"]', "content", dictionary.meta.description);
  setMeta('meta[property="og:locale"]', "content", language === "es" ? "es_ES" : "en_GB");

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const value = translate(dictionary, element.dataset.i18n);
    if (value) element.textContent = value;
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const value = translate(dictionary, element.dataset.i18nPlaceholder);
    if (value) element.setAttribute("placeholder", value);
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
}

function getSavedLanguage() {
  const saved = localStorage.getItem(languageKey);
  return translations[saved] ? saved : "es";
}

function translate(dictionary, path) {
  return path.split(".").reduce((value, key) => value?.[key], dictionary);
}

function setMeta(selector, attribute, value) {
  const element = document.querySelector(selector);
  if (element) element.setAttribute(attribute, value);
}

function clean(value) {
  return String(value || "").trim();
}
